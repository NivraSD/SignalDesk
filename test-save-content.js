async function testSaveContent() {
  const testContent = {
    type: 'blog-post',
    title: 'Test Blog Post',
    content: 'This is a test blog post content generated by NIV.',
    organization_id: 'test-org',
    metadata: {
      generatedBy: 'niv',
      timestamp: new Date().toISOString()
    }
  };

  console.log('Testing content save...\n');

  try {
    const response = await fetch('http://localhost:3003/api/content-library/save', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json'
      },
      body: JSON.stringify({
        content: testContent,
        metadata: testContent.metadata
      })
    });

    const result = await response.json();

    if (result.success) {
      console.log('✅ Content saved successfully!');
      console.log('ID:', result.id);
      console.log('Location:', result.location);
    } else {
      console.log('❌ Save failed:', result.error);
      if (result.details) {
        console.log('Details:', result.details);
      }
    }
  } catch (error) {
    console.error('Error:', error);
  }
}

testSaveContent();